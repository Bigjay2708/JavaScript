let arr = [1, 2, 3]; 
arr.push(4);  // [1, 2, 3, 4] Adds 4 to the end of the array
console.log(arr);

arr.pop();    // [1, 2, 3]  Removes 4 from the end of the array
console.log(arr);


const arr1 = [5, 6];
const arr2 = [7, 8];
const combinedArr = arr1.concat(arr2);  // [1, 2, 3, 4] Combines arr1 and arr2 into a new array called combinedArr 
console.log(combinedArr);
